// Extract Clause 3 "Terms and definitions" from BS EN 18031-1/2/3:2024 and emit
// src/lib/decision-trees/definitions.ts. Verbatim normative definitions
// (Notes/Examples/Source refs omitted). Union across the three parts; where a
// term's definition differs materially between parts (e.g. "security function"),
// each variant is kept with the list of standards it applies to. Trivial diffs
// (trailing punctuation, bracketed reference numbers) are merged.
import fs from "fs";

const SOURCES = [
  { std: 1, file: "pdf1.txt" },
  { std: 2, file: "pdf2.txt" },
  { std: 3, file: "pdf3.txt" },
];

const isNoise = (l) =>
  /^BS EN 18031/.test(l) ||
  /^EN 18031-\d:2024 \(E\)$/.test(l) ||
  /^-{1,3} \d+ of \d+ -{1,3}$/.test(l) ||
  /^\d{1,3}$/.test(l.trim());

const NUM = /^3\.\d+(?:\.\d+)?$/;
const STOP = /^(Note\b|NOTE\b|EXAMPLE\b|\[SOURCE)/;

const norm = (d) =>
  d
    .replace(/\[\d+\]/g, "")
    .replace(/\s+/g, " ")
    .replace(/[.\s]+$/, "")
    .trim()
    .toLowerCase();

// term(lc) -> normDef -> { stds:Set, definition, clause }
const terms = new Map();

for (const { std, file } of SOURCES) {
  const lines = fs
    .readFileSync(file, "utf8")
    .split(/\r?\n/)
    .map((l) => l.replace(/ /g, " "));
  const start = lines.findIndex((l) => /^3\s+Terms and definitions\s*$/.test(l));
  if (start < 0) continue;
  let end = lines.findIndex((l, i) => i > start && /^4\s+[A-Za-z]/.test(l));
  if (end < 0) end = lines.length;

  let i = start + 1;
  while (i < end) {
    const line = lines[i].trim();
    if (NUM.test(line)) {
      const clause = `EN 18031-${std} §${line}`;
      let j = i + 1;
      while (j < end && (isNoise(lines[j]) || !lines[j].trim())) j++;
      const term = (lines[j] || "").trim();
      const def = [];
      let k = j + 1;
      while (k < end) {
        const t = lines[k].trim();
        if (isNoise(lines[k])) {
          k++;
          continue;
        }
        if (!t || NUM.test(t) || STOP.test(t)) break;
        def.push(t);
        k++;
      }
      const definition = def
        .join(" ")
        .replace(/([A-Za-z.\]])-\s+(\d)/g, "$1-$2")
        .replace(/\s+/g, " ")
        .trim();
      if (term && definition) {
        const key = term.toLowerCase();
        if (!terms.has(key)) terms.set(key, { term, variants: new Map() });
        const variants = terms.get(key).variants;
        const nk = norm(definition);
        if (!variants.has(nk))
          variants.set(nk, { stds: new Set(), definition, clause });
        variants.get(nk).stds.add(std);
      }
      i = k;
      continue;
    }
    i++;
  }
}

const sorted = [...terms.entries()].sort((a, b) => a[0].localeCompare(b[0]));
const body = sorted
  .map(([key, { variants }]) => {
    const arr = [...variants.values()].sort(
      (a, b) => Math.min(...a.stds) - Math.min(...b.stds),
    );
    const inner = arr
      .map(
        (v) =>
          `    { stds: [${[...v.stds].sort().join(", ")}], clause: ${JSON.stringify(
            v.clause,
          )}, definition: ${JSON.stringify(v.definition)} },`,
      )
      .join("\n");
    return `  ${JSON.stringify(key)}: [\n${inner}\n  ],`;
  })
  .join("\n");

const ts = `// EN 18031 — Terms and definitions (Clause 3), BS EN 18031-1/2/3:2024.
//
// AUTO-GENERATED by parse-definitions.mjs. Verbatim normative definitions
// (Notes/Examples omitted). Injected as grounding context so the AI interprets
// standard terms exactly as defined — not from prior knowledge. Some terms (e.g.
// "security function") are defined differently per standard; each variant lists
// the standards it applies to.
//
// To regenerate: node parse-definitions.mjs

export type DefinitionVariant = {
  stds: number[]; // EN 18031 parts this definition applies to (1, 2, 3)
  clause: string;
  definition: string;
};

export const DEFINITIONS: Record<string, DefinitionVariant[]> = {
${body}
};

// Render definitions as a compact grounding block. If \`std\` is given, only the
// variant applicable to that standard is shown per term. If \`terms\` is given,
// only those terms are included.
export function definitionsText(std?: 1 | 2 | 3, termList?: string[]): string {
  const keys = termList
    ? termList.map((t) => t.toLowerCase()).filter((t) => t in DEFINITIONS)
    : Object.keys(DEFINITIONS);
  const out: string[] = [];
  for (const k of keys) {
    const variants = DEFINITIONS[k];
    const v = std ? variants.find((x) => x.stds.includes(std)) : variants[0];
    if (!v) continue;
    out.push(\`- \${k} (\${v.clause}): \${v.definition}\`);
  }
  return out.join("\\n");
}
`;

fs.writeFileSync("src/lib/decision-trees/definitions.ts", ts, "utf8");
const multi = sorted.filter(([, v]) => v.variants.size > 1).map(([k]) => k);
console.log(`terms: ${sorted.length}`);
console.log(`multi-variant (per-standard) terms: ${multi.join(", ") || "none"}`);
